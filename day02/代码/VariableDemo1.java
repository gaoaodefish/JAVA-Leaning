public class VariableDemo1{
		//叫做main方法，表示程序的主入口，格式固定
        public static void main(String[] args){
			//定义变量
			//数据类型 变量名 = 数据值
			//数据类型：限定了变量能储存的数据的类型
			//int（整数）double（小数）
			//变量名：就是储存空间的名字
			//作用：方便日后使用
			//数据值：放在变量中的数据
			//等号：赋值，把右边的数据赋给左边的变量
			
			int a = 10;
			System.out.println(a);
        }
}